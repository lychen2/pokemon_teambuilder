function reasonEntries(roleText) {
  return Object.fromEntries(
    Object.entries(roleText)
      .filter(([key]) => key.startsWith("analysis.roleDesc."))
      .map(([key, value]) => [key.replace("analysis.roleDesc.", "analysis.roleReason."), value]),
  );
}

