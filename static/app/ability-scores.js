const NORMALIZED_ABILITY_SCORES = new Map(
  Object.entries(RAW_ABILITY_SCORES).map(([name, score]) => [normalizeName(name), score]),
);

export function getAbilityReferenceScore(abilityName) {
  if (!abilityName) {
    return 0;
  }
  return NORMALIZED_ABILITY_SCORES.get(normalizeName(abilityName)) || 0;
}

export function getScaledAbilityScore(abilityName, cap, floor = -cap) {
  const referenceScore = getAbilityReferenceScore(abilityName);
  if (!cap) {
    return 0;
  }
  const scaled = (referenceScore / 100) * cap;
  return Math.min(cap, Math.max(floor, scaled));
}

export function getAbilityScoreEntries() {
  return new Map(NORMALIZED_ABILITY_SCORES);
}
