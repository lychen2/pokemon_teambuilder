function interpolate(template, params) {
  return template.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ""));
}

export function normalizeLanguage(language) {
  return LANGUAGES.has(language) ? language : DEFAULT_LANGUAGE;
}

export function t(language, key, params = {}) {
  const safeLanguage = normalizeLanguage(language);
  const template = TRANSLATIONS[safeLanguage][key] ?? TRANSLATIONS[DEFAULT_LANGUAGE][key] ?? key;
  return interpolate(template, params);
}

export function applyStaticTranslations(language) {
  const safeLanguage = normalizeLanguage(language);
  document.documentElement.lang = safeLanguage === "en" ? "en" : "zh-CN";
  document.title = t(safeLanguage, "page.title");
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(safeLanguage, node.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.placeholder = t(safeLanguage, node.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    node.setAttribute("aria-label", t(safeLanguage, node.dataset.i18nAriaLabel));
  });
}

